    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            // what happens when i press search
            
            
            String wrdSearch = txtSearch.getText();

            String querySearch = "SELECT * FROM entries WHERE (word=?)";
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dictionary","root","@zx12cv36");
            pt = conn.prepareStatement(querySearch);
            pt.setString(1, wrdSearch);
            rs = pt.executeQuery();
            
            if(rs.next())
            {
                
                LblError.setText(" ");
//                LblError.setVisible(false);
                String w = rs.getString("word");
                String t = rs.getString("wordtype");
                String h = rs.getString("definition");
                
                
                txtAreaSearch.append(w + ": ");
                
                txtAreaSearch.append(t+" \n"+ h + "\n\n");
                
                while(rs.next())
                {
                    LblError.setText(" ");
//                    LblError.setVisible(false);
                    w = rs.getString("word");
                    t = rs.getString("wordtype");
                    h = rs.getString("definition");

                    txtAreaSearch.append(w + ": ");
                    
                    txtAreaSearch.append(t+" \n"+ h + "\n\n");

                }
            }
            else
            {
//                LblError.setVisible(true);
                LblError.setText("oops :( word not found ");
            }
            
            
            
            
        } catch (SQLException ex) {
//            LblError.setVisible(true);
            Logger.getLogger(SearchView.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

